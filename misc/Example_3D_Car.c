// This function deals with double buffering and drawing of 3D objects
void RenderWorld(ObjStructType *obj,ObjStructType *ball1,ObjStructType *ball2,ObjStructType *ball3,WorldStructType0 *theWorld)
{
	// This variable keeps track of the current buffer for double buffering
	int currentBuffer;
	//get the current buffer
	currentBuffer=GsGetActiveBuff();
	// Set the viewing system
	GsSetRefView2(&view);
	// Set the address of the packet area that will contain drawing commands
	GsSetWorkBase((PACKET*)out_packet[currentBuffer]);
	// clear the ordering table
	GsClearOt(0, 0, &othWorld[currentBuffer]);
	// Draw the world
	DrawWorld(theWorld,  &othWorld[currentBuffer]);


	// rotate the root ball hierarchy, to make them all spin on top of the car
	RotateModel (&ball1->gsObjectCoord,&ball1->rotation, 0, 64, 0);

	// Draw the balls
	DrawObj(obj, &othWorld[currentBuffer]);
	// Draw the player
	DrawObj(ball1, &othWorld[currentBuffer]);


	DrawObj(ball2, &othWorld[currentBuffer]);

	DrawObj(ball3, &othWorld[currentBuffer]);

	// wait for end of drawing
	DrawSync(0);
	// wait for V_BLANK interrupt
	vsyncInterval=VSync(0);
	// print the vSync interval
	FntPrint(0, "VSync %d  Car %d:%d\n",vsyncInterval, obj->gsObjectCoord.coord.t[0], obj->gsObjectCoord.coord.t[2]);
	// force text output
	FntFlush(-1);
	// Swap The Buffers
	GsSwapDispBuff();
	// register clear-command: clear to black
	GsSortClear(0,0,0,&othWorld[currentBuffer]);
	// register request to draw ordering table
	GsDrawOt(&othWorld[currentBuffer]);
}
/*
 *
 From Peter Passmore 3D course pdf page 15
 Setting up lighting is pretty straight forward, in fact its so easy we’ll have two. A light is
defined by the GsF_LIGHT struct which defines a light source by the direction in which it is
pointing, and it’s intensity expressed as a red, green, blue triple. Note these lights can be
considered to be infinitely far away, so we don’t specify their positions only the direction in
which they are emitting light. This direction is represented by a vector whose components
can be any size (ie: they are not normaliased values). We store our two lights in an array
defined as follows:
 *
 */
void InitialiseLight(GsF_LIGHT *flLight, int nLight,
		int nX, int nY, int nZ,
		int nRed, int nGreen, int nBlue)
{
	// Set the direction in which the light travels
	flLight->vx = nX;flLight->vy = nY;    flLight->vz = nZ;
	// Set the colour
	flLight->r = nRed; flLight->g = nGreen; flLight->b = nBlue;
	// Activate light
	GsSetFlatLight(nLight, flLight);
}

void InitialiseTrackerView(GsRVIEW2 *view, int nProjDist, int nRZ,
		int nVPX, int nVPY, int nVPZ,
		int nVRX, int nVRY, int nVRZ)
{
	// This is the distance between the eye
	// and the imaginary projection screen
	GsSetProjection(nProjDist);
	// Set the eye position or center of projection
	view->vpx = nVPX; view->vpy = nVPY;  view->vpz = nVPZ;
	// Set the look at position
	view->vrx = nVRX; view->vry = nVRY; view->vrz = nVRZ;
	// Set which way is up
	view->rz=-nRZ;
	// Set the origin of the coord system in this case the car
	view->super = &theCar.gsObjectCoord ;
	// Activate view
	GsSetRefView2(view);
}

